const ALL: Record<LangCode, T> = { en, gu, hi, fr, de, zh };

interface LangContextValue {
  lang: LangCode;
  setLang: (l: LangCode) => void;
  t: (key: string) => string;
}

const LangContext = createContext<LangContextValue | undefined>(undefined);

export function LanguageProvider({ children }: { children: ReactNode }) {
  const stored = (typeof localStorage !== "undefined" && localStorage.getItem("quantex_lang") as LangCode) || "en";
  const [lang, setLangState] = useState<LangCode>(stored);

  const setLang = (l: LangCode) => {
    setLangState(l);
    localStorage.setItem("quantex_lang", l);
  };

  const t = (key: string): string => {
    return ALL[lang]?.[key] ?? ALL.en[key] ?? key;
  };

  return <LangContext.Provider value={{ lang, setLang, t }}>{children}</LangContext.Provider>;
}

export function useLanguage() {
  const ctx = useContext(LangContext);
  if (!ctx) throw new Error("useLanguage must be used within LanguageProvider");
  return ctx;
}
